/// Chunk size for the dedicated upload stream. Larger than the old 64KB
/// SyncStream chunk since there is no per-chunk CRC/framing overhead here
/// -- just raw bytes the receiver appends -- so bigger reads mean fewer
/// awaits without hurting backpressure (the stream still flow-controls).
const UPLOAD_CHUNK_BYTES: usize = 256 * 1024;

/// A fresh transfer id. Generated by the caller (not inside `send_file`)
/// so it can register a cancel handle *before* the send starts, closing
/// the race where a cancel arrives before the id is known.
pub fn new_transfer_id() -> String {
    uuid::Uuid::new_v4().to_string()
}

/// Walks an error's `source()` chain looking for `needle`. The TLS
/// verifier's rejection reason (e.g. the TOFU FINGERPRINT_CHANGED marker)
/// is nested several layers deep inside a `tonic::transport::Error`, so the
/// top-level `Display` alone does not reveal it.
fn error_chain_mentions(err: &(dyn std::error::Error + 'static), needle: &str) -> bool {
    let mut current: Option<&(dyn std::error::Error + 'static)> = Some(err);
    while let Some(e) = current {
        if e.to_string().contains(needle) {
            return true;
        }
        current = e.source();
    }
    false
}

/// Whole-file SHA-256 (hex), computed by streaming the file so a large
/// upload never buffers it in RAM just to declare its hash in
/// `PrepareUpload`. Matches the daemon receiver's own streaming digest
/// byte-for-byte.
async fn hash_file(path: &Path) -> Result<String> {
    let mut file = tokio::fs::File::open(path).await?;
    let mut hasher = Sha256::new();
    let mut buf = vec![0u8; UPLOAD_CHUNK_BYTES];
    loop {
        let n = file.read(&mut buf).await?;
        if n == 0 {
            break;
        }
        hasher.update(&buf[..n]);
    }
    Ok(hex::encode(hasher.finalize()))
}

